void Physics::Tick(double dt)
{
	GarbageCollector::Tick();
	for (Body* b : data)
		b->Tick(dt);
}


//	*****************************************  //
//	**               Body                  **  //
//	*****************************************  //

Body::Body(Entity* parent) :
	parent(parent)
{ }

void Body::Tick(double dt)
{
	if (!(alive = parent->alive))
		return;
}
